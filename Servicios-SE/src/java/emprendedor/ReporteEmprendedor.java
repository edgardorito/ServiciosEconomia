package emprendedor;

/**
 *
 * @author ERIDE21
 */
public class ReporteEmprendedor {
    private String nombreProyecto;
    private String tipoProyecto;
    private String ciudad;
    private String direccion;
    private int cantidadAsignada;
    private int cantidadGastadaMes;


    /**
     * @return the nombreProyecto
     */
    public String getNombreProyecto() {
        return nombreProyecto;
    }

    /**
     * @param nombreProyecto the nombreProyecto to set
     */
    public void setNombreProyecto(String nombreProyecto) {
        this.nombreProyecto = nombreProyecto;
    }

    /**
     * @return the tipoProyecto
     */
    public String getTipoProyecto() {
        return tipoProyecto;
    }

    /**
     * @param tipoProyecto the tipoProyecto to set
     */
    public void setTipoProyecto(String tipoProyecto) {
        this.tipoProyecto = tipoProyecto;
    }

    /**
     * @return the ciudad
     */
    public String getCiudad() {
        return ciudad;
    }

    /**
     * @param ciudad the ciudad to set
     */
    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the cantidadAsignada
     */
    public int getCantidadAsignada() {
        return cantidadAsignada;
    }

    /**
     * @param cantidadAsignada the cantidadAsignada to set
     */
    public void setCantidadAsignada(int cantidadAsignada) {
        this.cantidadAsignada = cantidadAsignada;
    }

    /**
     * @return the cantidadGastadaMes
     */
    public int getCantidadGastadaMes() {
        return cantidadGastadaMes;
    }

    /**
     * @param cantidadGastadaMes the cantidadGastadaMes to set
     */
    public void setCantidadGastadaMes(int cantidadGastadaMes) {
        this.cantidadGastadaMes = cantidadGastadaMes;
    }
    
    
}
